        // We don't need no education
        // We don't need no thought control

        String lineOne = "We don't need no education\nWe don't need no thought control";
        System.out.println(lineOne);

        // Check "this" out!, "s inside of "s!

        String lineTwo = "Check \"this\" out!, \"s inside of \"s!";
